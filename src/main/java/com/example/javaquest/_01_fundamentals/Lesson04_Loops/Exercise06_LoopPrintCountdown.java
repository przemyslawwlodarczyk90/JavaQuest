package com.example.javaquest._01_fundamentals.Lesson04_Loops;

public class Exercise06_LoopPrintCountdown {
    public static void main(String[] args) {
        // ⏳ Wypisz liczby od 10 do 1 w dół (odliczanie)

        for (int i = 10; i>0; i--){
            System.out.println(i);
        }
    }
}
